DROP TABLE IF EXISTS post;
CREATE TABLE post (
	id SERIAL PRIMARY KEY,
	post_id BIGINT UNSIGNED NOT NULL,
	user_id BIGINT UNSIGNED NOT NULL,
	body text,
	filename VARCHAR(1000),
	size INT,
	metadata JSON,
	created_at DATETIME DEFAULT NOW(),
	updated_at DATETIME DEFAULT CURRENT_TIMESTAMP ON UPDATE CURRENT_TIMESTAMP,
	INDEX(user_id),
	FOREIGN KEY (user_id) REFERENCES users(id),
	FOREIGN KEY (post_id) REFERENCES post(id)
);

DROP TABLE IF EXISTS likes;
CREATE TABLE likes (
	id SERIAL PRIMARY KEY,
	to_user_like_id BIGINT UNSIGNED NOT NULL,
	from_user_like_id BIGINT UNSIGNED NOT NULL,
	media_id BIGINT UNSIGNED NOT NULL,
	user_id  BIGINT UNSIGNED NOT NULL,
	post_id BIGINT UNSIGNED NOT NULL,
	created_at DATETIME DEFAULT NOW(),
	INDEX likes_from_user_like_id(from_user_like_id),
	INDEX likes_to_user_like_id(to_user_like_id),
	INDEX likes_media_id(media_id),
	INDEX likes_post_id(post_id),
	INDEX likes_user_id(user_id),
	FOREIGN KEY (user_id) REFERENCES users(id),
	FOREIGN KEY (media_id) REFERENCES media(id),
	FOREIGN KEY (post_id) REFERENCES post(id)
);

